	// for문을 통해 전체 노트를 훑어보게 한다
	public void judge(String input) {
		
		// 만약 noteList.get(i)로 가져온 현재 노트가 사용자가 입력한 input과 일치한다면 note.judge() 메소드 실행
		// 만약 노트 입력이 안된거면 무시 -> 추후 miss 판정
		for (int i = 0; i < noteList.size(); i++) {
			Note note = noteList.get(i);
			if(input.equals(note.getNoteType())) {
				judgeEvent(note.judge());
				break;
			}
		}
	}
	
	// 판정 이벤트 설정
	// Perfect +1000, Great +500, Good +300, Bad +100, Miss +0
	public void judgeEvent(String judge) {
		if(judge.equals("Perfect")) {
			judgeImage = new ImageIcon(Main.class.getResource("../images/judgePerfect.png")).getImage();
			score += 1000;
			combo++;
			perfectCount++;
		} else if(judge.equals("Great")) {
			judgeImage = new ImageIcon(Main.class.getResource("../images/judgeGreat.png")).getImage();
			score += 500;
			combo++;
			greatCount++;
		} else if(judge.equals("Good")) {
			judgeImage = new ImageIcon(Main.class.getResource("../images/judgeGood.png")).getImage();
			score += 300;
			combo++;
			goodCount++;
		} else if(judge.equals("Bad")) {
			judgeImage = new ImageIcon(Main.class.getResource("../images/judgeBad.png")).getImage();
			score += 100;
			combo++;
			badCount++;
		} else if(judge.equals("Miss")) {
			judgeImage = new ImageIcon(Main.class.getResource("../images/judgeMiss.png")).getImage();
			score += 0;
			combo = 0;
			missCount++;
		}
	}
